package ifTest;
// 10번 : if~else문
public class IfTest02 {	// 클래스 중괄호 시작
	public static void main(String[] args) {	//main 메소드 중괄호 시작
		
		System.out.println("출력 시작");
		
		if(false) {	// if문영역
			System.out.println("조건식이 true입니다.");
		}else {	// else문 영역
			System.out.println("조건식이 false입니다.");
		}
		
		System.out.println("출력 끝");
		
	}	// main 메소드 중괄호 끝
}	// 클래스 중괄호 끝
